    async newTransport() {
        oauth2Client.setCredentials({
            refresh_token :  googleAppRefreshToken
        })
        const accessToken = oauth2Client.getAccessToken();
        return nodemailer.createTransport({

            service: "gmail",
            auth: {
                type : "OAuth2",
                user : senderEmail,
                clientId:  googleAppClientId,
                clientSecret:  googleAppClientSecret,
                refreshToken :  googleAppRefreshToken,
                accessToken
            }
        });

    }

    // Send the actual email
    async send(subject) {

        let url
        if (subject === `welcome`) {
            url = await this.welcomeMessage()
        }
        else if (subject === `DownloadLink`) {
            url = await this.downloadLink()
        }
        else if (subject === `passwordReset`){
            url = await this.resetCode()
        }
       
        
        // 2) Define email options
        const mailOptions = {
            from: this.from,
            to: this.to,
            subject,
            html: url,
            text: "An Email from Qubstore.com"
        };
        
        // 3) Create a transport and send email
        await this.newTransport().then(async (transport) => {
            console.log("sending........")
            await transport.sendMail(mailOptions);
        }).catch(err => {
            console.log(`error`, err.message)
            throw (err)
        })
    }

